                        <p>Cornish Property Services has a history spanning more than 108 years that has brought together some of the best-known names in the Services and Building Industries. With our roots firmly planted, the company has grown organically by acquisition and diversifying into a number of complementary services. These services are provided to business, industry, government and may be offered individually or in an integrated package to best meet client needs.</p>
                        <p>Cornish Property Services was founded by Bert Cornish in 1902. He purchased a large block of land on the Pacific Highway where he built stables for horses, carts and buggies used by the staff to run the business. This business was originally owned by the Australian Gaslight Company. In the early days Cornish Property Services was the only service business in the Ku-ring-gai area and our Head Office is still located just 200 meters from the Original Site on Pacific Highway Gordon.</p>
                        <p>The business grew heavily due to all the developments that were commencing in the area. Cornish Property Services has been named as the oldest Services and Building company in Sydney&rsquo;s North Shore. Bert Cornish was known to provide an old fashion service built on ethics and excellence. This man strived tirelessly even in difficult times, such as the wars, to continuously help the community, family and friends.</p>

                        <p>Decades later Allan Raad and John Morrison, who have extensive experience in the building and facilities management industries, acquired the company. Allan and John have expanded the business offering clients a &lsquo;one-stop-shop&rsquo; for property maintenance solutions, plumbing, electrical and building services.</p>
